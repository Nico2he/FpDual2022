public class Envio{

    protected String medio;
    protected int numFresco;
    protected int numRefrigerado;
    protected int numCongelado;

    public Envio(String medio, int numFresco, int numRefrigerado, int numCongelado) {

        this.medio = medio;
        this.numFresco = numFresco;
        this.numRefrigerado = numRefrigerado;
        this.numCongelado = numCongelado;

    }

}